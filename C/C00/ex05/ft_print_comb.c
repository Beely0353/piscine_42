#include <unistd.h>

void	comb(char *a);

void	ft_print_comb(void)
{
	char	c[5];

	c[0] = '0';
	c[1] = '1';
	c[2] = '2';
	c[3] = ',';
	c[4] = ' ';
	comb(c);
}

void	comb(char*a)
{
	while (a[0] != '8')
	{
		a[1] = a[0] + 1;
		while (a[1] != '9')
		{
			a[2] = a[1] + 1;
			while (a[2] != ':')
			{	
				if (a[0] != a[1] && a[1] != a[2] && a[2] != a[0])
				{
					if (a[0] != '7')
						write(1, a, 5);
					else
						write(1, a, 3);
				}
				a[2]++;
			}
			a[1]++;
		}
		a[0]++;
	}
}
